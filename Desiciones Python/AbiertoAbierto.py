x = float(input("Ingrese el valor de x: "))
a = float(input("Ingrese el valor de a: "))
b = float(input("Ingrese el valor de b: "))
c = float(input("Ingrese el valor de c: "))
d = float(input("Ingrese el valor de d: "))
e = float(input("Ingrese el valor de e: "))
f = float(input("Ingrese el valor de f: "))

if x>a and x<b:
    print("Su valor esta dentro del primer rango")
else:
    if x>c and x<d:
        print("Su valor esta dentro del segundo rango")
    else:
        if x>e and x<f:
            print("Su valor esta dentro del tercer rango")
